def add(a, b) -> float:
    """
    adds together parameters
    :param a: first number
    :param b: second number
    :return: sum of the two numbers
    """
    egg = a + b
    return egg


def subtract(a, b) -> float:
    """
    subtracts one parameter from the other
    :param a: first number
    :param b: second number
    :return: result of the two numbers
    """
    egg = a - b
    return egg


def multiply(a, b) -> float:
    """
    multiplies two parameters together
    :param a: first number
    :param b: second number
    :return: product of the two numbers
    """
    egg = a * b
    return egg


def divide(a, b) -> float:
    """
    divides first parameter by second
    :param a: first number
    :param b: second number
    :return: result of the first number divided by the second
    """
    egg = a / b
    return egg
